<h3>What Are Cookies</h3>
  <p>
    As is common practice with almost all professional websites this site uses cookies, which are tiny files that are downloaded to your computer, to improve your experience. This page describes what information they gather, how we use it and why we sometimes need to store these cookies. We will also share how you can prevent these cookies from being stored however this may downgrade or 'break' certain elements of the sites functionality.
    <br />
    For more general information on cookies see the Wikipedia article on HTTP Cookies...
  </p>
  
  <br /><br />
  <h3>How We Use Cookies</h3>
  <p>
    We use cookies for a variety of reasons detailed below. 
    Unfortunately in most cases there are no industry standard options for disabling 
    cookies without completely disabling the functionality and features they add to this site. 
    <br />
    It is recommended that you leave on all cookies if you are not sure whether you need them or not in case they are used to provide a service that you use.
  </p>

  <br /><br />
  <h3>Disabling Cookies</h3>
  <p>
     You can prevent the setting of cookies by adjusting the settings on your browser (see your browser Help for how to do this). 
     Be aware that disabling cookies will affect the functionality of this and many other websites that you visit. 
     <br />
     Disabling cookies will usually result in also disabling certain functionality and features of the this site. 
     Therefore it is recommended that you do not disable cookies.
  </p>

  <br /><br />
  <h3> The Cookies We Set</h3>
  <p>
    If you create an account with us then we will use cookies for the management of the signup process and general administration. These cookies will usually be deleted when you log out however in some cases they may remain afterwards to remember your site preferences when logged out.
  </p>
  <br />
  <p>
    We use cookies when you are logged in so that we can remember this fact. This prevents you from having to log in every single time you visit a new page. These cookies are typically removed or cleared when you log out to ensure that you can only access restricted features and areas when logged in.
  </p>
  <br />
  <p>
    This site offers newsletter or email subscription services and cookies may be used to remember if you are already registered and whether to show certain notifications which might only be valid to subscribed/unsubscribed users. 
  </p>
  <br />
  <p>
    When you submit data to through a form such as those found on contact pages or comment forms cookies may be set to remember your user details for future correspondence.
  </p>

  <br /><br />
  <h3>Third Party Cookies</h3>
  <p>
    In some special cases we also use cookies provided by trusted third parties. The following section details which third party cookies you might encounter through this site.
  </p>
  <p>
    This site uses Google Analytics which is one of the most widespread and trusted analytics solution on the web for helping us to understand how you use the site and ways that we can improve your experience. These cookies may track things such as how long you spend on the site and the pages that you visit so we can continue to produce engaging content.
  </p>
  <br />
  <p>
    For more information on Google Analytics cookies, see the official Google Analytics page.
  </p>
  <br />
  <p>
    The <a href="https://support.google.com/adsense/answer/2839090" target="_blank">DoubleClick</a> cookie is used by Google in ads that are served on the websites of its partners - such as websites displaying AdSense ads, or participating in Google certified ad networks. When users visit a partner's website and either view or click on an ad, a cookie may be dropped on the end user's browser.
  </p>
  
